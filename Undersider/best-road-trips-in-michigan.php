            <h2>1. Red Arrow Highway</h2>
            <p>A road trip from New Buffalo to St. Joseph along Michigan’s southwest coastline offers a scenic and charming journey full of delightful stops and experiences. This picturesque route along Lake Michigan is dotted with sandy beaches, rolling dunes, and quaint lakeside towns. You'll have the chance to visit wineries, fruit farms, and farmers' markets that showcase the region's agricultural richness. The vibrant arts scene in St. Joseph, combined with its waterfront parks, restaurants, and charming downtown, make it a great destination. The drive is short but packed with opportunities to relax, explore, and enjoy the beautiful lakeshore. </p>
