            <h2>2. Keweenaw Peninsula</h2>
            <p>The Keweenaw Peninsula in Michigan's Upper Peninsula is a must-visit for nature lovers and history buffs alike. Famous for its rugged beauty, it's surrounded by the pristine waters of Lake Superior and offers unparalleled outdoor adventures like hiking, kayaking, and exploring ancient forests. The region's rich copper mining history adds cultural depth, with fascinating historical sites like the Quincy Mine and the charming town of Calumet. You'll also discover stunning waterfalls, secluded beaches, and breathtaking views at places like Brockway Mountain Drive. The Keweenaw's mix of natural wonder and heritage makes it a perfect getaway for those seeking adventure and tranquility.</p>
